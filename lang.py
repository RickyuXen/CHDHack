# Wrap functionality around class
class Lang:
    def __init__(self):
        self.command = ""

    def print_letter(self, letter_matrix):
        for row in letter_matrix:
            line = ''.join(['#' if col == 1 else ' ' for col in row])
            print(line)
    
    def parse_code(self, code):
        self.command = code.split("|")
        print(self.command)

    def interpret_code(self, code):
        self.parse_code(code)
        com = self.command[0]
        match com:
            case "P":
                print(self.command[1])
            case "Pr":
                print(self.command[1][::-1])
            case "Pri":
                print(self.command[1][::-1][::-1])
            case "Pa":
                eachLetter = list(self.command[1].replace(" ", "").upper())
                
                # Initialize a list to hold each row of the final output
                output_rows = [""] * 5  # Assuming each letter is 5 rows tall

                for letter in eachLetter:
                    if letter in ascii_alphabet:
                        letter_rows = self.print_letter(ascii_alphabet[letter])
                        # Append each row of the letter to the corresponding row in output
                        for i in range(5):  # Assuming each letter has 5 rows
                            output_rows[i] += letter_rows[i] + " "  # Add a space between letters
                    else:
                        return "Error: Letter not found"

                # Join all rows with newlines for the final output
                return "\n".join(output_rows)

print(""" Syntax:
      P|[input] -> prints out [input]
      Pr|[input] -> prints out [input] in reverse
      Pri|[input] -> prints out [input] in reverse
      Pa|[input] -> prints out [input] in ASCII
""")
